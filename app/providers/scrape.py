"""Headless-browser scrape provider for parcelsapp.com (no API key).

parcelsapp.com aggregates 600+ couriers and exposes the result of a lookup via
a single POST to ``https://parcelsapp.com/api/v2/parcels``. That request is
heavily anti-bot protected: the tracking number is obfuscated client-side, an
``x-csrf-token`` is attached, and a large browser-fingerprint blob (``se=``) is
sent — all generated by the page's own JavaScript. Rather than reverse-engineer
that scheme (fragile), we drive the real page with Playwright so the browser
builds the signed request itself, and we simply intercept the JSON response.

Crucial detail: parcelsapp detects headless Chromium (the ``sec-ch-ua`` brand
leaks ``"HeadlessChrome"``) and answers ``{"error":"NO_DATA"}``. Launching the
*system Edge* channel (``SCRAPE_BROWSER_CHANNEL=msedge``, the default) reports a
genuine browser brand and returns real data. The bundled Chromium fallback will
usually be blocked.

Design notes
------------
* Playwright's sync API can't run inside the asyncio event loop, and on Windows
  its teardown clashes with uvicorn's Proactor loop. So all browser work runs in
  ONE dedicated daemon thread that owns the Playwright instance + a persistent
  browser context, and jobs are serialized through a queue (a context is not
  concurrency-safe). The async ``track()`` submits a job and awaits its result
  without blocking the event loop.

This path is inherently best-effort: any failure raises ``ProviderError`` so the
resolver can return an honest "not found" (or fall back to demo data when
``SCRAPE_FALLBACK_MOCK=true``).
"""
